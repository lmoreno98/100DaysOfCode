import turtle as t

# Move the cursor the the position
t.goto(-100,-100)

# Dont draw wen you move the cursor
t.penup()

# Draw wen you move the cursor
t.pendown()

# Change the line color
t.color("blue")

# Change the size of the line
t.pensize(2)

# Moving speed
t.speed(50)

# Change background color
t.getscreen().bgcolor("blue")

# Direction movings
t.forward(300)
t.left(90)
t.right(180)
